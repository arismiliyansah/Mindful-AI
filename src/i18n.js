const interp = (str, vars) => {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : `{${k}}`));
};

export function isValidLang(lang) {
  return LANGS.includes(lang);
}

export function makeT(lang) {
  const safeLang = isValidLang(lang) ? lang : 'id';
  return (key, vars) => {
    const dict = STRINGS[safeLang] || STRINGS.id;
    const raw = dict[key] ?? STRINGS.en[key] ?? STRINGS.id[key] ?? key;
    return interp(raw, vars);
  };
}

const LangContext = createContext({ lang: 'id', setLang: () => {}, t: makeT('id') });

export function I18nProvider({ lang, setLang, children }) {
  useEffect(() => {
    if (typeof document !== 'undefined') {
      document.documentElement.lang = isValidLang(lang) ? lang : 'id';
    }
  }, [lang]);

  const value = {
    lang: isValidLang(lang) ? lang : 'id',
    setLang,
    t: makeT(lang),
  };
  return createElement(LangContext.Provider, { value }, children);
}

export function useT() {
  return useContext(LangContext);
}

export function localeFor(lang) {
  switch (lang) {
    case 'en': return 'en-US';
    case 'zh-CN': return 'zh-CN';
    case 'zh-TW': return 'zh-TW';
    default: return 'id-ID';
  }
}
